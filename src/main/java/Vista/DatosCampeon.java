    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = txtNombre.getText();
        String email = txtEmail.getText();
        String rol = this.comboRol.getSelectedItem().toString();
        String dificultad = this.comboDificultad.getSelectedItem().toString();
        
        if (nombre.isEmpty() || email.isEmpty() || rol.isEmpty() || dificultad.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Complete los campos");
        } else {
            
            boolean confirmacion = this.principal.campeonDuplicado(nombre);
        
            if(confirmacion){
                JOptionPane.showMessageDialog(null, "esta duplicado");
            }else{
                this.principal.agregarCampeon(nombre, email, rol, dificultad);
                JOptionPane.showMessageDialog(null, "Campeon Guardado con exito");
                
                txtNombre.setText("");
                txtEmail.setText("");
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

